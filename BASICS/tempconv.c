// Program to convert temprature from degree centigrade to Fahrenheit.

#include <stdio.h>

int main(void) {
    float Celsius, Fahrenheit;

    printf("Enter the temp in Celsius: ");
    scanf("%f", &Celsius);

    printf("Temprature in Fahrenheit: %.3f", Fahrenheit = (1.8 * Celsius) + 32);
    return 0;
}